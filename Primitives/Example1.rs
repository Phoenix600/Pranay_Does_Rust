fn main()
{
    // Numerical Types In Rust 
    // Integer Number With The Unsigned Bits 
    println!("1+2 = {}",1u32+2);

    // Integer Number With The Signed Bits 
    println!("1-2 = {}",1i32-2);

    // Scientif Notation 
    println!("1e4 is {0}...-2.5e4 is {1}....",1e4,-2.5e4);

    // Getting Started With Boolean Types 
    println!("TRUE : {0}....FALSE:...{1}",true,false);
    
    // Short Circuiting Boolean Logic
    println!("true AND false : {}", true && false);
    println!("true OR false : {}",true || false);
    println!("NOT true : {}",!true);




}
